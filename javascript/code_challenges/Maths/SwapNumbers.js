function SwapNumbers(num_1, num_2) {
	console.log("Before swapping:\n num_1 = " + num_1 + "\n num_2=" + num_2)
	var temp = num_1;
	num_1 = num_2;
	num_2 = temp
	console.log("After swapping:\n num_1 = " + num_1 + "\n num_2=" + num_2)
}

